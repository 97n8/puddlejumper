export function isKnownEventType(eventType: string): boolean {
  return KNOWN_EVENT_TYPES.has(eventType as ArchieveEventTypeValue);
}

export function validateEventData(eventType: string, data: unknown): void {
  if (!isKnownEventType(eventType)) {
    throw new ArchieveError(ARCHIEVE_INVALID_EVENT, `Unknown event type: ${eventType}`);
  }
  const required = REQUIRED_DATA_FIELDS[eventType];
  if (!required || required.length === 0) return;
  if (!data || typeof data !== 'object') {
    throw new ArchieveError(ARCHIEVE_INVALID_EVENT, `event.data must be an object for ${eventType}`);
  }
  const dataObj = data as Record<string, unknown>;
  for (const field of required) {
    if (dataObj[field] === undefined || dataObj[field] === null) {
      throw new ArchieveError(ARCHIEVE_INVALID_EVENT, `Missing required data field '${field}' for event type ${eventType}`);
    }
  }
}
